public class Task3 { // New class for Task 3

    public static void main(String[] args) {
        System.out.println("Task 3: Count up by 3 from 0 to 18");
        System.out.println("----------------------------------");

        for (int i = 0; i <= 18; i += 3) { // i += 3 increments by 3
            System.out.print(i + " ");
        }
        System.out.println("\n"); //
    }
}