public class Task2 { // New class for Task 2

    public static void main(String[] args) {
        System.out.println("Task 2: Count down by 1 from 30 to 0");
        System.out.println("------------------------------------");

        for (int i = 30; i >= 0; i--) {
            System.out.print(i + " ");
        }
        System.out.println("\n"); // Add a newline for better spacing for next task
    }
}